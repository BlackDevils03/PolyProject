    private void inputDataHoDon(List<QLHoaDon> listqlhd) {
        dtm2.setRowCount(0);
        int i = 0;
        for (QLHoaDon qlhd : listqlhd) {
            i++;
            dtm2.addRow(qlhd.toRow(i));
        }
        
    }

    private void inPutsanPham(List<QLChiTietSP> qlsp) {
        dtm1.setRowCount(0);
        int i = 0;
        for (QLChiTietSP qLctsp : qlsp) {
            i++;
            dtm1.addRow(qLctsp.torowBH(i));
        }
    }
    private void inPutGioHang(List<QLGioHangChiTiet> Listqlgh){
        dtm.setRowCount(0);
        for (QLGioHangChiTiet qlgh : Listqlgh) {
            dtm.addRow(qlgh.toRow());
        }
    }

