private void filldata(int i){
    QLHoaDon qlhd = listqlhd.get(i);
    txtMaHD.setText(qlhd.getMa());
    txtNgayTao.setText(qlhd.getNgayTao());
    IQLNhanVienService iqlnvs = new NhanVienService();
    QLNhanVien qlnv = new QLNhanVien(qlhd.getIdNV().getId());
    IQLHoaDonChiTietService iqlhdcts =  new HoaDonChiTietService();
    int s1 = iqlhdcts.getone(qlhd.getIdNV().getId()).getSoLuong();
    BigDecimal s2 = iqlhdcts.getone(qlhd.getIdNV().getId()).getDonGia();
    String s = iqlnvs.getone(qlnv).getTen();
    double x = Double.valueOf(String.valueOf(s2));
    txtTenNV.setText(s);
    txtTongTien.setText(String.valueOf(s1 *  x));
}
