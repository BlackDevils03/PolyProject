    private void fillData(int i) {
        QLCuaHang qlch = listch.get(i);
        txtMaCH.setText(qlch.getMa());
        lbid.setText(qlch.getId());
        txtTP.setText(qlch.getThanhPho());
        txtTenCH.setText(qlch.getTen());
        txtQG.setText(qlch.getQuocGia());
        txtDiaChi.setText(qlch.getDiaChi());
    }

