private void loaddDate() {
        listch = iqlchs.getALL();
        inPutData(listch);
    }

    private void inPutData(List<QLCuaHang> listCH) {
        dtm.setRowCount(0);
        for (QLCuaHang qLCuaHang : listCH) {
            dtm.addRow(qLCuaHang.toRow());
        } 
    }
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int i = jtbcuahang.getSelectedRow();
        QLCuaHang qlch = listch.get(i);
        if (iqlchs.remove(qlch.getId()) == true) {
            JOptionPane.showMessageDialog(this, "Remove Complete");
            loaddDate();

        } else {
            JOptionPane.showMessageDialog(this, "Remove Faild");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QLCuaHang qlch = new QLCuaHang(null, txtMaCH.getText(), txtTenCH.getText(), txtDiaChi.getText(), txtTP.getText(), txtQG.getText());
        if (iqlchs.save(qlch) == true) {
            JOptionPane.showMessageDialog(this, "Save Complete");
            loaddDate();
        } else {
            JOptionPane.showMessageDialog(this, "Save Faild");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int i = jtbcuahang.getSelectedRow();
        QLCuaHang qlch1 = listch.get(i);
        QLCuaHang qlch = new QLCuaHang(qlch1.getId(), txtMaCH.getText(), txtTenCH.getText(), txtDiaChi.getText(), txtTP.getText(), txtQG.getText());

        if (iqlchs.update(qlch) == true) {
            JOptionPane.showMessageDialog(this, "Update Complete");
            loaddDate();
        } else {
            JOptionPane.showMessageDialog(this, "Update Faild");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

