    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String gioiTinh = "";
        if (rdoNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nữ";
        }

        String idCv = iqlcvs.getone((String) cbbchucvu.getSelectedItem());
        String idCh = iqlchs.getone((String) cbbCuaHang.getSelectedItem());
        QLCuaHang qlch = new QLCuaHang(idCh);
        QLChucVu qlcv = new QLChucVu(idCv);
        int tranghai = 0;
        if (cbbtrangThai.getSelectedItem().equals("Active")) {
            tranghai = 1;
        } else {
            tranghai = 0;
        }
        int i = jtbNhanVien.getSelectedRow();
        String id = listQLNV.get(i).getId();
        QLNhanVien qlnv = new QLNhanVien(id, txtMa.getText(), txtTen.getText(), txtTenDem.getText(), txtH.getText(),
                gioiTinh, txtNgaySinh.getText(), txtDiaChi.getText(), txtSDT.getText(), txtMK.getText(), qlch, qlcv, null, tranghai);
        iqlnvs.Update(qlnv);
        loadData();
    }//GEN-LAST:event_btnSuaActionPerformed

