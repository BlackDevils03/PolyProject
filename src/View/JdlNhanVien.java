    private void loadData() {
        listQLNV = iqlnvs.getAll();
        inPutData(listQLNV);
    }

    private void loadcbbtrangthai() {
        cbbtrangThai.setModel(dcbm1);
        List<String> listcbbtt = new ArrayList<>();
        dcbm1.removeAllElements();
        listcbbtt.add("Active");
        listcbbtt.add("No Active");
        dcbm1.addAll(listcbbtt);
        dcbm1.setSelectedItem(listcbbtt.get(0));
    }

    private void loadcbbcuahang() {
        cbbCuaHang.setModel(dcbm2);
        List<String> listcbbch = new ArrayList<>();
        dcbm2.removeAllElements();
        listch = iqlchs.getALL();
        for (QLCuaHang qlch : listch) {
            listcbbch.add(qlch.getTen());
//        System.out.println(qlch.toString());
        }
        dcbm2.addAll(listcbbch);
        dcbm2.setSelectedItem(listcbbch.get(0));
    }

    private void loadcbbcv() {
        cbbchucvu.setModel(dcbm);
        dcbm.removeAllElements();
        List<String> listcbbtcv = new ArrayList<>();
        listCV = iqlcvs.getall();
        for (QLChucVu qLChucVu : listCV) {
            listcbbtcv.add(qLChucVu.getTen());
        }
        dcbm.addAll(listcbbtcv);
        dcbm.setSelectedItem(listcbbtcv.get(0));
    }

    private void inPutData(List<QLNhanVien> listQLNV) {
        dtm.setRowCount(0);
        for (QLNhanVien qLNhanVien : listQLNV) {
            dtm.addRow(qLNhanVien.torow());
        }
    }

