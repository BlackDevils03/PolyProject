    private void fillData(int i) {
        QLNhanVien qlnv = listQLNV.get(i);
        txtTen.setText(qlnv.getTen());
        txtTenDem.setText(qlnv.getTenDem());
        txtDiaChi.setText(qlnv.getDiaChi());
        txtH.setText(qlnv.getHo());
        txtSDT.setText(qlnv.getSdt());
        txtNgaySinh.setText(qlnv.getNgaySinh());
        txtMK.setText(qlnv.getMatKhau());
        txtMa.setText(qlnv.getMa());
        cbbCuaHang.setSelectedItem(qlnv.getIdCV().getTen());
        if (qlnv.getTrangThai() == 1) {
            cbbtrangThai.setSelectedItem("Active");
        } else {
            cbbtrangThai.setSelectedItem("No Active");
        }
        for (QLChucVu qlcv : listCV) {
            if (qlcv.getId().equals(qlnv.getIdCV().getId())) {
                cbbchucvu.setSelectedItem(qlcv.getTen());
                System.out.println(qlcv.getId());
            }
        }

        if (qlnv.getGioiTinh().equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        for (QLCuaHang qlch : listch) {
            if (qlch.getId().equals(qlnv.getIdCH().getId())) {
                cbbCuaHang.setSelectedItem(qlch.getTen());
            }
        }
    }
